    public void initUser() {
        FileHandler fh = new FileHandler();
        fNameLbl.setText(firstName);
        lNameLbl.setText(lastName);
        user = fh.getUser(firstName, lastName);
        if (user == null) {
            dispose();
            JOptionPane.showMessageDialog(this, "Auto Login failed!\nReturning to the login page");
            LoginHandler lh = new LoginHandler();
            lh.forget();
            LoginView lv = new LoginView();
            lv.setVisible(true);
        } else {
            if (user.getTransactions().isEmpty()) {
                jTextArea1.setText("Nothing yet");
            } else {
                ArrayList list = user.getTransactions();
                for (int i = list.size() - 1; i >= 0; i--) {
                    jTextArea1.append(String.format("%s\n", list.get(i)));
                }
            }
            balanceLbl.setText(String.format("%.2f â‚¬", user.getBalance()));
        }
    }

